package OPERADORES; //IGNORAR ESTO

public class comparacion
{ public static void main(String[] args) {
    int a = 3, b = 2;

    //igualdad
    var resultado = a == b;
    System.out.println("resultado de a==b = " + resultado);

    //Distinto
    resultado = a != b;
    System.out.println("resultado de a!=b = " + resultado);

    //Mayor que
    resultado = a > b;
    System.out.println("resultado de a>b = " + resultado);

    //Menor que
    resultado = a < b;
    System.out.println("resultado de a>b = " + resultado);
}
}
