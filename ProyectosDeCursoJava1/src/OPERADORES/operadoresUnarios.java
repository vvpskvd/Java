package OPERADORES; //IGNORAR ESTO

public class operadoresUnarios
{
    public static void main(String[] args) {
        int a = 2 , b = -5 , resultado = 0;

        //incremento
        a++;
        resultado = a;
        System.out.println("resultado con Incremento = " + resultado); //el resultado debe ser 3

        //Decremento
        b--;
        resultado = b;
        System.out.println("resultado con Decremento = " + resultado); //el resultado debe ser -6
    }
}
